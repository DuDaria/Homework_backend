# Задание-3:
# Напишите скрипт, заполняющий указанный файл (самостоятельно задайте имя файла)
# произвольными целыми цифрами, в результате в файле должно быть
# 2500-значное произвольное число.
# Найдите и выведите самую длинную последовательность одинаковых цифр
# в вышезаполненном файле.

import os
import random

number = 2500

rand_list = [random.randint(0, 9) for i in range(number)]

lst = ''.join(list(map(lambda x: str(x), rand_list)))

path = os.path.join('number_is_2500')

with open(path, 'w', encoding='UTF-8') as w:  
    w.write(lst)
    
with open(path, 'r', encoding='UTF-8') as r:
    lst_read = list(r.read())

new_lst_read = list(map(lambda x: int(x), rand_list))

# Вывод с помощью groupby
from itertools import groupby
max_num = max((list(k) for g, k in groupby(new_lst_read)), key=len)
print(max_num)

# Наибольшее кол-во последовательных цифр через for
result = 1
max_result = 0
first_elem = new_lst_read[0]

for i in new_lst_read[1:]:
    if i == first_elem:
        result += 1
    else:
        if result > max_result:
            max_result = result
        first_elem = i
        result = 1
        
print("Максимальная последовательность =", max_result)
